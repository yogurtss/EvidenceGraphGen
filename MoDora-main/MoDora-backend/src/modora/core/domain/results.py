from __future__ import annotations

import json
from dataclasses import dataclass
from typing import Any, Iterable


@dataclass(frozen=True)
class ResultItem:
    """A single evaluation result item.

    Represents a single question from the dataset along with its corresponding model prediction and judgment result.

    Attributes:
        questionId: Unique identifier for the question.
        pdf_id: Associated PDF filename or ID.
        question: Text content of the question.
        answer: List of standard answers. Supports single or multiple answers.
        tag: Category tag of the question (optional).
        prediction: Prediction result generated by the model. None indicates no answer generated.
        judge: Judgment result ('T' for correct, 'F' for incorrect).
    """

    questionId: int
    pdf_id: str
    question: str
    answer: list[str]
    tag: str | None
    prediction: str | None
    judge: str | None

    @staticmethod
    def from_dict(record: dict[str, Any]) -> "ResultItem":
        """Parse and validate ResultItem from a dictionary object.

        Args:
            record: Raw data dictionary.

        Returns:
            Validated ResultItem instance.

        Raises:
            TypeError: Raised when the data type is invalid or required fields are missing.
        """
        if not isinstance(record, dict):
            raise TypeError("Data item must be a dictionary object")

        qid = record.get("questionId")
        if not isinstance(qid, int):
            raise TypeError("questionId must be an integer type")

        question = record.get("question")
        if not isinstance(question, str) or not question.strip():
            raise TypeError("question must be a non-empty string")

        ans = record.get("answer")
        if ans is None:
            raise TypeError("answer field is missing")
        if isinstance(ans, str):
            answer = [ans]
        elif isinstance(ans, list) and all(isinstance(x, str) for x in ans):
            answer = ans
        else:
            raise TypeError("answer must be a string or a list of strings")

        pred = record.get("prediction")
        if pred is not None and not isinstance(pred, str):
            raise TypeError("prediction must be a string or null")
        if isinstance(pred, str) and not pred.strip():
            raise TypeError("prediction cannot be an empty string")

        pdf_id = record.get("pdf_id")
        if not isinstance(pdf_id, str) or not pdf_id:
            raise TypeError("pdf_id must be a non-empty string")

        tag = record.get("tag")
        if tag is not None and not isinstance(tag, str):
            raise TypeError("tag must be a string or null")

        judge = record.get("judge")
        if judge is not None and not isinstance(judge, str):
            raise TypeError("judge must be a string or null")
        if isinstance(judge, str) and judge not in {"T", "F"}:
            raise TypeError("judge must be 'T' or 'F'")
        if pred is None and judge != "F":
            raise TypeError("When prediction is null, judge must be 'F'")

        return ResultItem(
            questionId=qid,
            pdf_id=pdf_id,
            question=question,
            answer=answer,
            tag=tag,
            prediction=pred,
            judge=judge,
        )

    def to_dict(self) -> dict[str, Any]:
        """Serializes the current item into a dictionary.

        Returns:
            dict[str, Any]: A dictionary containing all fields, suitable for JSON/JSONL persistence.
        """
        return {
            "questionId": self.questionId,
            "pdf_id": self.pdf_id,
            "question": self.question,
            "answer": self.answer,
            "tag": self.tag,
            "prediction": self.prediction,
            "judge": self.judge,
        }


def write_results_file(path: str, items: Iterable[ResultItem]) -> int:
    """Writes a list of result items to a JSONL file.

    Args:
        path (str): The target file path.
        items (Iterable[ResultItem]): An iterable object of ResultItem to be written.

    Returns:
        int: The number of lines successfully written.
    """
    written = 0
    with open(path, "w", encoding="utf-8") as f:
        for it in items:
            f.write(json.dumps(it.to_dict(), ensure_ascii=False) + "\n")
            written += 1
    return written
